    public boolean isNisnExist(String noNisn) {
        //database koneksi
        Connection con = ConnectionProvider.getCon();
        // SQL query utk pengecekan nisn yang sudah ada
        String perintah_SQL = "SELECT nisn AS count FROM student HAVING count = ?";
        try (PreparedStatement statement = con.prepareStatement(perintah_SQL)) {
            //mengatur index tabel pada SQL
            statement.setString(1, noNisn);
            try (ResultSet rs = statement.executeQuery()) {
                if (rs.next()) {
                    int count = rs.getInt(1);
                    return count > 0;
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        return false;
    }

