    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String noNisn = jTextField1.getText().trim();
        String nisn = jTextField1.getText();
        String nama = jTextField2.getText();
        String kelas = jTextField3.getText();
        String kelamin = (String) jComboBox1.getSelectedItem();
        String nohp = jTextField4.getText();
        String email = jTextField5.getText();
        String benar = "0";
        String nilaiakhir = "0";

        try {
            Connection con = ConnectionProvider.getCon();
            JFrame jf = new JFrame();
            jf.setAlwaysOnTop(true);
            int a = JOptionPane.showConfirmDialog(jf, "Yakin data sudah benar?", "Select", JOptionPane.YES_NO_OPTION);
            if (a == 0) {
                //memeriksa kolom yg belum terisi
                if (jTextField1.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(jf, "NISN siswa belum lengkap", "Peringatan", JOptionPane.WARNING_MESSAGE);
                } else if (jTextField2.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(jf, "Nama siswa belum lengkap", "Peringatan", JOptionPane.WARNING_MESSAGE);
                } else if (jTextField3.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(jf, "Kelas siswa belum lengkap", "Peringatan", JOptionPane.WARNING_MESSAGE);
                } else if (jTextField4.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(jf, "NoHP siswa belum lengkap", "Peringatan", JOptionPane.WARNING_MESSAGE);
                } else if (jTextField5.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(jf, "Email siswa belum lengkap", "Peringatan", JOptionPane.WARNING_MESSAGE);
                } else {
                    // memeriksa apakah no.Nisn dan kelas sudah ada dalam database
                    if (isNisnExist(noNisn)) {
                        //Nisn dan kelas sudah ada, menampilkan pesan peringatan
                        JOptionPane.showMessageDialog(jf, "Data siswa sudah ada di database!", "Peringatan", JOptionPane.WARNING_MESSAGE);
                        setVisible(false);
                        new studentDetails().setVisible(true);
                    } else {
                        PreparedStatement ps = con.prepareStatement("insert into student values (?,?,?,?,?,?,?,?)");
                        ps.setString(1, nisn);
                        ps.setString(2, nama);
                        ps.setString(3, kelas);
                        ps.setString(4, kelamin);
                        ps.setString(5, nohp);
                        ps.setString(6, email);
                        ps.setString(7, benar);
                        ps.setString(8, nilaiakhir);
                        ps.executeUpdate();
                        JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
                        setVisible(false);
                        new instructionStudent(nisn).setVisible(true);
                    }
                }
            } else {
                setVisible(true);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jTextField4KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField4KeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        if (!(Character.isDigit(c) || (c == KeyEvent.VK_BACK_SPACE) || c == KeyEvent.VK_DELETE)) {
            getToolkit().beep();
            evt.consume();
        }
    }//GEN-LAST:event_jTextField4KeyTyped

    private void jTextField1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyPressed
        // TODO add your handling code here:
        //action saat key pressed
        //get string pada jtextfield
        String nisn = jTextField1.getText();
        //mengecek panjang string
        int length = nisn.length();
        char c = evt.getKeyChar();

        //mengecek index 0 - 9 pada string
        if (evt.getKeyChar() >= '0' && evt.getKeyChar() <= '9') {
            //mengecek panjang tdk lebih dari 10digit
            if (length < 10) {
                jTextField1.setEditable(true);
            } else {
                getToolkit().beep();
                jTextField1.setEditable(false);
            }
        } else {
            //memperbolehkan keys backspace dan delete untuk edit
            if (evt.getExtendedKeyCode() == KeyEvent.VK_BACK_SPACE || evt.getExtendedKeyCode() == KeyEvent.VK_DELETE) {
                jTextField1.setEditable(true);
            } else {
                jTextField1.setEditable(false);
            }
        }
    }//GEN-LAST:event_jTextField1KeyPressed

